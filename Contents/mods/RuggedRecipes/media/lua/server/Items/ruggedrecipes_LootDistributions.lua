-- JAR LID FIX
function Recipe.OnCreate.OpenJarGetLid (items, result, player)	
  local inv = player:getInventory();	
  inv:AddItem("Base.EmptyJar");	
  inv:AddItem("Base.JarLid");	
end

function Recipe.OnCreate.OpenJarGetOnlyLid (items, result, player)	
  local inv = player:getInventory();	
  inv:AddItem("Base.JarLid");	
end

